# program that declares different types of variables and prints their types

# Integer variable
num_students = 25  
print(f"The variable 'num_students' is of type {type(num_students)}.")  # Prints its type

# Float variable
pi_value = 3.14159  
print(f"The variable 'pi_value' is of type {type(pi_value)}.")  # Prints its type

# String variable
course_name = "Programming 101"  
print(f"The variable 'course_name' is of type {type(course_name)}.")  # Prints its type